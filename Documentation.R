
##Application - Number of people going to Mumbai daily
The Purpose of this application is to determine the number of people going to Mumbai from different cities of India by using different mode of transport like Flight, Train, Bus, Taxi ec.

###Step-1 - Slide to select the number of flights going to Mumbai daily.
###Step-2 - Slide to select the number of trains going to Mumbai daily.
###Step-3 - Slide to select the number of buses going to Mumbai daily.
###Step-4 - Slide to select the number of taxis going to Mumbai daily.



